/**
 * Generate AI coaching suggestions based on today's nutrition vs targets.
 */
export function generateNutritionSuggestions(
  totals: MacroSummary,
  targets: MacroSummary,
  timeOfDay: number = new Date().getHours()
): NutritionSuggestion[] {
  const suggestions: NutritionSuggestion[] = [];
  const remaining = {
    kcal: targets.kcal - totals.kcal,
    protein: targets.protein - totals.protein,
    carbs: targets.carbs - totals.carbs,
    fat: targets.fat - totals.fat,
    water: (targets.water || 3000) - totals.water,
  };

  // Protein check
  if (remaining.protein > 40) {
    suggestions.push({
      id: 'protein_low',
      type: 'warning',
      message: `You still need ${Math.round(remaining.protein)}g protein. Add chicken, eggs, or paneer to your next meal.`,
      priority: 10,
    });
  } else if (remaining.protein < -10) {
    suggestions.push({
      id: 'protein_over',
      type: 'tip',
      message: `Protein target exceeded by ${Math.abs(Math.round(remaining.protein))}g. Great job on hitting your protein goal!`,
      priority: 5,
    });
  } else if (remaining.protein <= 20 && remaining.protein > 0) {
    suggestions.push({
      id: 'protein_close',
      type: 'success',
      message: `Almost there! Just ${Math.round(remaining.protein)}g more protein needed today.`,
      priority: 7,
    });
  }

  // Calorie balance
  if (remaining.kcal < -300) {
    suggestions.push({
      id: 'kcal_over',
      type: 'warning',
      message: `Caloric surplus of ${Math.abs(Math.round(remaining.kcal))} kcal. Consider lighter meals for the rest of the day.`,
      priority: 9,
    });
  } else if (remaining.kcal > 500 && timeOfDay >= 18) {
    suggestions.push({
      id: 'kcal_under_evening',
      type: 'warning',
      message: `Caloric deficit detected: ${Math.round(remaining.kcal)} kcal remaining. Eating too little may increase catabolism risk.`,
      priority: 8,
    });
  }

  // Fat check
  if (remaining.fat < -15) {
    suggestions.push({
      id: 'fat_over',
      type: 'tip',
      message: 'Reduce fat intake today. Choose grilled over fried and opt for low-fat proteins.',
      priority: 6,
    });
  }

  // Water check
  if (remaining.water > 1000) {
    suggestions.push({
      id: 'water_low',
      type: 'tip',
      message: `Drink ${Math.round(remaining.water / 1000 * 10) / 10}L more water today. Hydration improves performance and recovery.`,
      priority: 8,
    });
  } else if (remaining.water <= 0) {
    suggestions.push({
      id: 'water_done',
      type: 'success',
      message: 'Daily hydration goal achieved. Hunter vitality maintained.',
      priority: 3,
    });
  }

  // Carb balance
  if (totals.carbs / targets.carbs >= 0.9 && totals.carbs / targets.carbs <= 1.1) {
    suggestions.push({
      id: 'carb_good',
      type: 'success',
      message: 'Good carbohydrate balance. Energy systems optimal for training.',
      priority: 2,
    });
  }

  // Pre-workout nutrition window (11am-2pm if user works out in afternoon)
  if (timeOfDay >= 11 && timeOfDay <= 14 && totals.kcal < 400) {
    suggestions.push({
      id: 'pre_workout',
      type: 'coach',
      message: 'Pre-workout nutrition window: Consume 400-600 kcal with complex carbs 90-120 min before training.',
      priority: 7,
    });
  }

  return suggestions.sort((a, b) => b.priority - a.priority);
}

/**
 * Generate a daily meal plan based on user goals and preferences.
 */
export function generateMealPlan(
  targetKcal: number,
  targetProtein: number,
  targetCarbs: number,
  targetFat: number,
  preferences: {
    vegetarian?: boolean;
    vegan?: boolean;
    includeIndian?: boolean;
  } = {}
): MealPlanItem[] {
  const { vegetarian = false, vegan = false, includeIndian = true } = preferences;

  const distribution = {
    breakfast: 0.25,
    'pre-workout': 0.15,
    lunch: 0.30,
    'post-workout': 0.10,
    dinner: 0.20,
  };

  const proteinFoods = vegan
    ? ['tofu', 'lentils', 'chickpeas', 'tempeh']
    : vegetarian
    ? ['paneer', 'cottage cheese', 'greek yogurt', 'eggs', 'tofu']
    : ['chicken breast', 'eggs', 'fish', 'paneer', 'greek yogurt'];

  const plans: MealPlanItem[] = [
    {
      mealType: 'breakfast',
      foods: [],
      totalKcal: Math.round(targetKcal * distribution.breakfast),
      totalProtein: Math.round(targetProtein * 0.25),
      note: includeIndian
        ? 'Oatmeal / Poha / Upma + Greek Yogurt + Fruits'
        : 'Oatmeal + Whey Protein + Fruits',
    },
    {
      mealType: 'pre-workout',
      foods: [],
      totalKcal: Math.round(targetKcal * distribution['pre-workout']),
      totalProtein: Math.round(targetProtein * 0.10),
      note: 'Banana + Handful of Almonds / Dry fruits',
    },
    {
      mealType: 'lunch',
      foods: [],
      totalKcal: Math.round(targetKcal * distribution.lunch),
      totalProtein: Math.round(targetProtein * 0.30),
      note: includeIndian
        ? `${proteinFoods[0].charAt(0).toUpperCase() + proteinFoods[0].slice(1)} + Rice/Roti + Dal + Salad`
        : `${proteinFoods[0].charAt(0).toUpperCase() + proteinFoods[0].slice(1)} + Rice + Vegetables`,
    },
    {
      mealType: 'post-workout',
      foods: [],
      totalKcal: Math.round(targetKcal * distribution['post-workout']),
      totalProtein: Math.round(targetProtein * 0.15),
      note: 'Protein shake + Banana OR Curd Rice',
    },
    {
      mealType: 'dinner',
      foods: [],
      totalKcal: Math.round(targetKcal * distribution.dinner),
      totalProtein: Math.round(targetProtein * 0.20),
      note: includeIndian
        ? `${proteinFoods[1]?.charAt(0).toUpperCase() + (proteinFoods[1] ?? 'fish').slice(1)} + 2 Rotis + Sabzi + Curd`
        : 'Grilled protein + Quinoa/Sweet potato + Veggies',
    },
  ];

  return plans;
}

