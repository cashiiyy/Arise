// ─── Dataset Loading ─────────────────────────────────────────────────────────

// We load CSV text via require — Expo/Metro can bundle text files with
// an appropriate transformer, but to avoid configuration changes, we embed
// the data as pre-parsed JSON arrays generated by a build script.
// Fallback: load from a bundled JSON version if CSVs aren't directly readable.

let _indianFoods: FoodItem[] | null = null;
let _globalFoods: FoodItem[] | null = null;
let _allFoods: FoodItem[] | null = null;

/**
 * Load and parse the Indian food dataset.
 * CSV columns: Dish Name, Calories (kcal), Carbohydrates (g), Protein (g),
 *              Fats (g), Free Sugar (g), Fibre (g), Sodium (mg), Calcium (mg),
 *              Iron (mg), Vitamin C (mg), Folate
 */
function loadIndianFoods(): FoodItem[] {
  if (_indianFoods) return _indianFoods;

  try {
    // Try loading from pre-built JSON (generated by build script)
    const data = require('../../Nutrition/indian_foods_data.json');
    _indianFoods = data;
    return _indianFoods!;
  } catch {
    // Fallback: return a curated subset of common Indian foods
    _indianFoods = INDIAN_FOOD_FALLBACK;
    return _indianFoods;
  }
}

/**
 * Load and parse the global food dataset.
 */
function loadGlobalFoods(): FoodItem[] {
  if (_globalFoods) return _globalFoods;

  try {
    const data = require('../../Nutrition/global_foods_data.json');
    _globalFoods = data;
    return _globalFoods!;
  } catch {
    _globalFoods = GLOBAL_FOOD_FALLBACK;
    return _globalFoods;
  }
}

/**
 * Get all foods (combined Indian + global).
 */
function getAllFoods(): FoodItem[] {
  if (_allFoods) return _allFoods;
  _allFoods = [...loadIndianFoods(), ...loadGlobalFoods()];
  return _allFoods;
}

// ─── Search ──────────────────────────────────────────────────────────────────

/**
 * Fuzzy search across all food datasets.
 * Returns results ranked by match quality.
 */
export function searchFood(query: string, limit: number = 20): FoodItem[] {
  if (!query || query.trim().length < 2) return [];

  const q = query.toLowerCase().trim();
  const words = q.split(/\s+/);
  const all = getAllFoods();

  const scored = all
    .map(food => {
      const name = food.name.toLowerCase();
      let score = 0;

      // Exact match
      if (name === q) score += 100;
      // Starts with query
      else if (name.startsWith(q)) score += 80;
      // Contains full query
      else if (name.includes(q)) score += 60;
      // All words present
      else if (words.every(w => name.includes(w))) score += 40;
      // Any word present
      else if (words.some(w => name.includes(w) && w.length > 2)) score += 20;

      // Boost Indian foods slightly for Indian queries
      if (food.source === 'indian' && isLikelyIndianQuery(q)) score += 10;

      return { food, score };
    })
    .filter(({ score }) => score > 0)
    .sort((a, b) => b.score - a.score)
    .slice(0, limit)
    .map(({ food }) => food);

  // If no results, use AI estimation
  if (scored.length === 0) {
    const estimated = estimateByAI(query);
    if (estimated) return [estimated];
  }

  return scored;
}

function isLikelyIndianQuery(query: string): boolean {
  const indianKeywords = ['curry', 'dal', 'roti', 'rice', 'biryani', 'sabzi', 'paneer',
    'dosa', 'idli', 'sambar', 'chai', 'lassi', 'raita', 'naan', 'paratha',
    'aloo', 'gobi', 'masala', 'chana', 'rajma', 'kadhi', 'upma', 'poha'];
  return indianKeywords.some(kw => query.includes(kw));
}

/**
 * Get nutritional info for a specific food item, scaled to a serving size.
 */
export function getNutrition(foodItem: FoodItem, servingG: number): FoodItem {
  const ratio = servingG / (foodItem.servingG || 100);
  return {
    ...foodItem,
    servingG,
    kcal: Math.round(foodItem.kcal * ratio),
    protein: Math.round(foodItem.protein * ratio * 10) / 10,
    carbs: Math.round(foodItem.carbs * ratio * 10) / 10,
    fat: Math.round(foodItem.fat * ratio * 10) / 10,
    fiber: foodItem.fiber ? Math.round(foodItem.fiber * ratio * 10) / 10 : 0,
    sugar: foodItem.sugar ? Math.round(foodItem.sugar * ratio * 10) / 10 : 0,
    sodium: foodItem.sodium ? Math.round(foodItem.sodium * ratio) : 0,
  };
}

